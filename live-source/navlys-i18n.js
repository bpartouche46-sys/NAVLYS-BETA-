  /* cache des dicos « lazy » déjà chargés : code -> objet {cléFR: trad} */
  var LAZY = {};
  function isLazy(l){ return !!(LANG_META[l] && LANG_META[l].lazy); }
  function globalDictName(l){ return 'NAVLYS_DICT_'+l.toUpperCase(); }
  function loadDict(l, done){
    if(LAZY[l]){ if(done) done(); return; }
    var g = window[globalDictName(l)];
    if(g){ LAZY[l]=g; if(done) done(); return; }
    var s=document.createElement('script');
    s.src='/navlys-i18n-'+l+'.js';
    s.onload=function(){ LAZY[l]=window[globalDictName(l)]||{}; if(done) done(); };
    s.onerror=function(){ LAZY[l]={}; if(done) done(); };
    (document.head||document.documentElement).appendChild(s);
  }

  /* ---------- utilitaires ---------- */
  function norm(s){ return String(s).replace(/\s+/g,' ').replace(/^\s+|\s+$/g,''); }
  function leadWs(s){ var m=String(s).match(/^\s*/); return m?m[0]:''; }
  function trailWs(s){ var m=String(s).match(/\s*$/); return m?m[0]:''; }

  /* dictionnaire actif selon la langue cible (null en FR = source) */
  function dictFor(lang){
    if(lang==='fr') return null; /* FR : source, pas de traduction */
    if(lang==='en') return DICT;
    if(lang==='ru') return DICT_RU;
    return LAZY[lang]||{}; /* langues lazy : dico chargé (ou {} le temps du chargement) */
  }

  /* renvoie la traduction de `raw` vers LANG (espaces de bord préservés) ou null.
     La CLÉ est toujours la phrase française normalisée ; `raw` doit donc être le
     texte FR d'origine (pas une traduction déjà appliquée). */
  function translateRaw(raw){
    var key=norm(raw);
    if(!key) return null;
    var d=dictFor(LANG);
    if(!d) return null; /* FR : pas de traduction */
    var tr=d[key];
    if(tr==null || tr==='') return null;
    if(tr===key) return null; /* identique : rien à faire */
    return leadWs(raw)+tr+trailWs(raw);
  }

  /* ---------- traduction d'un noeud texte ----------
     __nvOrig = texte FR d'origine ; __nvLang = langue actuellement affichée. */
  function doNode(tn){
    if(!tn || tn.nodeType!==3) return;
    if(LANG==='fr'){
      /* réversibilité : on restaure toujours la source française */
      if(tn.__nvOrig!=null && tn.__nvLang && tn.__nvLang!=='fr'){
        tn.nodeValue=tn.__nvOrig; tn.__nvLang='fr';
      }
      return;
    }
    if(tn.__nvLang===LANG) return; /* déjà dans la bonne langue */
    /* on traduit TOUJOURS depuis le FR d'origine (même si EN/RU est affiché) */
    var src=(tn.__nvOrig!=null)?tn.__nvOrig:tn.nodeValue;
    var out=translateRaw(src);
    if(out!=null){
      if(tn.__nvOrig==null) tn.__nvOrig=tn.nodeValue;
      tn.nodeValue=out; tn.__nvLang=LANG;
    } else if(tn.__nvOrig!=null && tn.__nvLang!=='fr'){
      /* pas de traduction cible connue : on revient à la source FR */
      tn.nodeValue=tn.__nvOrig; tn.__nvLang='fr';
    }
  }

  /* ---------- traduction d'un attribut ---------- */
  function doAttr(el,name){
    if(!el || el.nodeType!==1 || !el.getAttribute) return;
    if(!el.hasAttribute(name)) return;
    var store=el.__nvAttr || (el.__nvAttr={});
    var slang=el.__nvAttrLang || (el.__nvAttrLang={});
    if(LANG==='fr'){
      if(store[name]!=null){ el.setAttribute(name,store[name]); slang[name]='fr'; }
      return;
    }
    if(slang[name]===LANG) return;
    var src=(store[name]!=null)?store[name]:el.getAttribute(name);
    var out=translateRaw(src);
    if(out!=null){
      if(store[name]==null) store[name]=el.getAttribute(name);
      el.setAttribute(name,out); slang[name]=LANG;
    } else if(store[name]!=null){
      el.setAttribute(name,store[name]); slang[name]='fr';
    }
  }

  function skipTag(nn){ return nn==='SCRIPT'||nn==='STYLE'||nn==='NOSCRIPT'; }

  /* ---------- parcours d'un sous-arbre ---------- */
  function translateSubtree(node){
    if(!node) return;
    if(node.nodeType===3){ doNode(node); return; }
    if(node.nodeType!==1) return;
    if(skipTag(node.nodeName)) return;
    var a;
    for(a=0;a<ATTRS.length;a++) doAttr(node,ATTRS[a]);
    /* noeuds texte descendants */
    if(document.createTreeWalker && typeof NodeFilter!=='undefined'){
      var walker=document.createTreeWalker(node,NodeFilter.SHOW_TEXT,{
        acceptNode:function(t){
          var p=t.parentNode;
          if(p && skipTag(p.nodeName)) return NodeFilter.FILTER_REJECT;
          return NodeFilter.FILTER_ACCEPT;
        }
      },false);
      var list=[], tn;
      while((tn=walker.nextNode())) list.push(tn);
      var k; for(k=0;k<list.length;k++) doNode(list[k]);
    }
    /* attributs des éléments descendants */
    if(node.querySelectorAll){
      var els=node.querySelectorAll('[placeholder],[title],[aria-label]');
      var e,b;
      for(e=0;e<els.length;e++){ for(b=0;b<ATTRS.length;b++) doAttr(els[e],ATTRS[b]); }
    }
  }

  function translateAll(){ translateSubtree(document.body); }

  /* ---------- observateur : traduit le contenu ajouté dynamiquement ---------- */
  function startObserver(){
    if(!window.MutationObserver) return;
    var obs=new MutationObserver(function(muts){
      if(LANG==='fr') return; /* en FR, la source est déjà en français */
      var i,j;
      for(i=0;i<muts.length;i++){
        var mu=muts[i];
        if(mu.type==='characterData'){ doNode(mu.target); }
        else if(mu.type==='attributes'){ doAttr(mu.target,mu.attributeName); }
        else if(mu.addedNodes){
          for(j=0;j<mu.addedNodes.length;j++) translateSubtree(mu.addedNodes[j]);
        }
      }
    });
    obs.observe(document.documentElement,{
      subtree:true, childList:true, characterData:true,
      attributes:true, attributeFilter:ATTRS
    });
  }

